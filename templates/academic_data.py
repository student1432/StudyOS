def get_syllabus(purpose, board_or_exam, grade=None, subjects=None):
    """
    Get the appropriate syllabus based on user's academic path
    
    Args:
        purpose: 'highschool' or 'exam' or 'after_tenth'
        board_or_exam: Board name (CBSE, ICSE) or Exam type (JEE, NEET)
        grade: Grade level (for highschool)
        subjects: List of subjects (for after_tenth)
    
    Returns:
        Dictionary containing syllabus structure
    """
    try:
        if purpose == 'highschool':
            if board_or_exam in ACADEMIC_SYLLABI['highschool']:
                if grade in ACADEMIC_SYLLABI['highschool'][board_or_exam]:
                    return ACADEMIC_SYLLABI['highschool'][board_or_exam][grade]
        
        elif purpose in ['exam', 'jee', 'neet']:
            # For exam preparation
            exam_type = board_or_exam if board_or_exam in ACADEMIC_SYLLABI['exams'] else 'JEE'
            if exam_type in ACADEMIC_SYLLABI['exams']:
                return ACADEMIC_SYLLABI['exams'][exam_type]
        
        elif purpose == 'after_tenth':
            # Use grade 11 or 12 syllabus from CBSE
            if grade and grade in ['11', '12']:
                full_syllabus = ACADEMIC_SYLLABI['highschool']['CBSE'][grade]
                # Filter by selected subjects if provided
                if subjects:
                    filtered = {}
                    for subject in subjects:
                        if subject in full_syllabus:
                            filtered[subject] = full_syllabus[subject]
                    return filtered if filtered else full_syllabus
                return full_syllabus
    
    except KeyError:
        pass
    
    # Return empty structure if not found
    return {}


def get_available_subjects(purpose, board_or_exam, grade=None):
    """Get list of available subjects for a given academic path"""
    syllabus = get_syllabus(purpose, board_or_exam, grade)
    return list(syllabus.keys()) if syllabus else []
